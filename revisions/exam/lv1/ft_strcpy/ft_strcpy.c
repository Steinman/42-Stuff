#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (dest[i] != '\0')
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int		main()
{
	char str1[] = "Hugo";
	char str2[] = "Robin le gros fdp";

	printf("%s", ft_strcpy(str1, str2));
	return (0);
}
