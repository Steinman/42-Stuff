char		*ft_strcat(char *dest, const char *src)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0')
		j++;
	k = i + j;
	j = 0;
	while (i <= k && src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
