int ft_atoi_nb(const char *str, int i)
{
	int sign;
	int nb;

	nb = 0;
	sign = 1;
	while (str[i] >= 0 && str[i] <= 32)
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * sign);
}
