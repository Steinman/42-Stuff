#include <stdlib.h>
#include "ft_list.h"

void	ft_list_push_front(t_list **begin_list, void *data)
{
	t_list *tmp;

	tmp = ft_create_elem(data);
	if (tmp)
	{
		tmp->next = begin_list[0]->next;
		begin_list[0]->next = tmp;
	}
}
