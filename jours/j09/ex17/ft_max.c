int		ft_max(int *tab, int length)
{
	int i;
	int max;

	i = 0;
	max = 0;
	while (i <= length)
	{
		if (tab[i] > max)
		{
			max = tab[i];
			i++;
		}
		else
			i++;
	}
	return (max);
}
